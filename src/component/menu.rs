use tui::widgets::ListState;

struct PopUpMenu<T> {
    state: ListState,
    items: Vec<T>,
}

impl<T> PopUpMenu<T> {
    pub fn from(items: Vec<T>) -> Self {
        Self {
            items,
            state: ListState::default(),
        }
    }

    fn next(&mut self) {
        let i = match self.state.selected() {
            Some(i) => {
                if i >= self.items.len() - 1 {
                    0
                } else {
                    i + 1
                }
            }
            None => 0,
        };
        self.state.select(Some(i));
    }

    fn previous(&mut self) {
        let i = match self.state.selected() {
            Some(i) => {
                if i == 0 {
                    self.items.len() - 1
                } else {
                    i - 1
                }
            }
            None => 0,
        };
        self.state.select(Some(i));
    }
}
